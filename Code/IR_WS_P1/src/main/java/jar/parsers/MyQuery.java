package model;

public class MyQuery {
    private String id;
    private String words;

    // Getters and setters for each field
    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getText() {
        return words;
    }

    public void setText(String words) {
        this.words = words;
    }
}
